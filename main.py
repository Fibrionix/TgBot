    current_question_idx = user_states[user_id]['current_question']
    
    # –û–±—Ä–∞–±–æ—Ç–∫–∞ –æ—Ç–º–µ–Ω—ã —Ç–µ—Å—Ç–∞
    if message.text == "‚ùå –û—Ç–º–µ–Ω–∏—Ç—å —Ç–µ—Å—Ç":
        cancel_test(message)
        return
    
    current_question = questions[current_question_idx]
    
