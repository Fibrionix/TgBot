def ask_question(message, question_num):
    """–ó–∞–¥–∞–µ—Ç –≤–æ–ø—Ä–æ—Å –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é"""
    user_id = message.from_user.id
    
    if question_num < len(questions):
        # –û–±–Ω–æ–≤–ª—è–µ–º —Ç–µ–∫—É—â–∏–π –≤–æ–ø—Ä–æ—Å
        user_states[user_id]['current_question'] = question_num
        
        # –°–æ–∑–¥–∞–µ–º –∫–ª–∞–≤–∏–∞—Ç—É—Ä—É —Å –≤–∞—Ä–∏–∞–Ω—Ç–∞–º–∏ –æ—Ç–≤–µ—Ç–æ–≤
        markup = telebot.types.ReplyKeyboardMarkup(
            resize_keyboard=True, 
            one_time_keyboard=True
        )
        
        for option in questions[question_num]["options"]:
            markup.add(option)
        
        # –î–æ–±–∞–≤–ª—è–µ–º –∫–Ω–æ–ø–∫—É –æ—Ç–º–µ–Ω—ã
        markup.add("‚ùå –û—Ç–º–µ–Ω–∏—Ç—å —Ç–µ—Å—Ç")
        
        # –ü–æ–∫–∞–∑—ã–≤–∞–µ–º –ø—Ä–æ–≥—Ä–µ—Å—Å
        progress = f"–í–æ–ø—Ä–æ—Å {question_num + 1} –∏–∑ {len(questions)}"
        progress_bar = "üü©" * (question_num + 1) + "‚¨ú" * (len(questions) - question_num - 1)
        
        question_text = (
            f"üìä {progress}\n"
            f"{progress_bar}\n\n"
            f"{questions[question_num]['text']}"
        )
        
        bot.send_message(message.chat.id, question_text, reply_markup=markup)
